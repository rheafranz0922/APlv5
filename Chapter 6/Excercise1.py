import math
#Find the ceil of an a
a = 2.3
ceil_result = math.ceil(a)
print(f"Ceil result of{a} is: {ceil_result}")

#find the floor of a=2.3.
floor_result = math.floor(a)
print(f"The floor result {a}is {floor_result}")

a = 5 #find the factoria of an a
factorial_result = math.factorial(a)
print(f"factorial result {a} is: {factorial_result}")

#Given the values
base = 2
exponent = 3
#calculate of 2^3 
result = base ** exponent
#The final result
print(f"The value of 2^3 is: {result}")

#find the square root of an a.
a = 16
squareroot_result = math.number(a)
print(f"square root of an {a}is {squareroot_result}")